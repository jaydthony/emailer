import mongoose from "mongoose";

const emailModelSchema = new mongoose.Schema({
  mailId: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  sender: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    required: true,
    default: Date.now,
  },
});

export const emailSchema = mongoose.model("messages", emailModelSchema);
